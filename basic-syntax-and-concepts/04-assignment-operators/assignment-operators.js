// 할당 연산자 예시
let name = "자바스크립트"; // 변수 name에 '자바스크립트'라는 값을 할당
let x = 5; // 변수 x에 5라는 값을 할당

x = x - 2; // x에서 2를 뺀 값을 다시 x에 할당
console.log(x); // 결과: 3

// 복합 할당 연산자 예시
x = 5;

x = x + 1;
x += 1; // 같은 의미

x = x + 2;
x += 2; // 같은 의미

x = x * 2;
x *= 2; // 같은 의미

x = x - 3;
x -= 3; // 같은 의미

x = x / 2;
x /= 2; // 같은 의미

x = x % 5;
x %= 5; // 같은 의미

// 증가/감소 연산자 예시
let score = 10;

score++; // score의 값을 1 증가시킴
console.log(score); // 결과: 11

score--; // score의 값을 1 감소시킴
console.log(score); // 결과: 10
