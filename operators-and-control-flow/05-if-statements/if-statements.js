// 물의 상태를 온도에 따라 조건문으로 출력하는 예제

// 1. 온도 변수 설정
let temperature = -5; // 초기 온도를 -5로 설정

// 2. 기본 if 문
// 조건: 온도가 0도 이하일 경우, '물이 얼었습니다.' 출력
if (temperature <= 0) {
  console.log("물이 얼었습니다."); // 조건 충족 시 출력
}

// 3. if-else 문
// 조건: 온도가 0도 이하일 경우 '물이 얼었습니다.' 출력, 아니면 '물이 얼지 않았습니다.' 출력
temperature = 15; // 온도를 15도로 변경
if (temperature <= 0) {
  console.log("물이 얼었습니다."); // 조건 충족 시 출력
} else {
  console.log("물이 얼지 않았습니다."); // 조건 불충족 시 출력
}

// 4. 중첩 if 문을 활용한 상태 확인
// 조건1: 온도가 0도 이하일 경우 '물이 얼었습니다.' 출력
// 조건2: 온도가 100도 이상일 경우 '물이 끓고 있습니다.' 출력
// 조건3: 그 외의 경우 '물이 얼지도 끓지도 않습니다.' 출력
temperature = 105; // 온도를 105도로 변경
if (temperature <= 0) {
  console.log("물이 얼었습니다.");
} else {
  if (temperature >= 100) {
    console.log("물이 끓고 있습니다.");
  } else {
    console.log("물이 얼지도 끓지도 않습니다.");
  }
}

// 5. else if 문으로 간결하게 여러 조건 처리
// 조건1: 온도가 0도 이하일 경우 '물이 얼었습니다.' 출력
// 조건2: 온도가 100도 미만일 경우 '물이 얼지도 끓지도 않습니다.' 출력
// 조건3: 온도가 100도 이상 150도 미만일 경우 '물이 끓고 있습니다.' 출력
// 조건4: 그 외의 경우 '물이 증기로 변했습니다.' 출력
temperature = 140; // 온도를 140도로 변경
if (temperature <= 0) {
  console.log("물이 얼었습니다.");
} else if (temperature < 100) {
  console.log("물이 얼지도 끓지도 않습니다.");
} else if (temperature < 150) {
  console.log("물이 끓고 있습니다.");
} else {
  console.log("물이 증기로 변했습니다.");
}
