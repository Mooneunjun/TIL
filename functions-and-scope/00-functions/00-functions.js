// 함수 선언: 여러 인사말을 출력하는 함수
function printGreetings() {
    console.log('안녕하세요');  // 한국어 인사
    console.log('Hello');       // 영어 인사
    console.log('Hola');        // 스페인어 인사
    console.log('Bonjour');     // 프랑스어 인사
    console.log('こんにちは');   // 일본어 인사
}

// 함수 호출
printGreetings();
printGreetings();

// 기본적인 함수 선언 및 호출 예시
function sayHi() {
    console.log('Hi!');
}

sayHi();

// 여러 명령어를 가진 함수 선언
